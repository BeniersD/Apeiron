@dataclass
class GCodeInstruction:
    """Simplified G-code instruction for 3D printing."""
    command: str  # e.g., "G1", "G0"
    x: Optional[float] = None
    y: Optional[float] = None
    z: Optional[float] = None
    e: Optional[float] = None  # extruder position

    def to_string(self) -> str:
        parts = [self.command]
        if self.x is not None:
            parts.append(f"X{self.x:.3f}")
        if self.y is not None:
            parts.append(f"Y{self.y:.3f}")
        if self.z is not None:
            parts.append(f"Z{self.z:.3f}")
        if self.e is not None:
            parts.append(f"E{self.e:.3f}")
        return " ".join(parts)


@dataclass
class DNAInstruction:
    """Simplified DNA sequence instruction for synthetic biology."""
    sequence: str  # e.g., "ATCG..."
    operation: str = "insert"  # "insert", "delete", "replace"
    target: Optional[str] = None


# ============================================================================
# Reaction Functor: translates reactions to physical instructions
# ============================================================================

class ReactionFunctor:
    """
    Functor F : ReactionCategory → InstructionCategory.

    Maps multisets (objects) to physical layouts/coordinates.
    Maps reactions (morphisms) to sequences of machine instructions
    (G-code, DNA, etc.).

    The functor preserves the monoidal structure: tensor product of
    reactions maps to parallel execution of instructions.
    """

    def __init__(self, reaction_category: ReactionCategory,
                 output_type: str = 'gcode'):
        self.reaction_category = reaction_category
        self.output_type = output_type
        self.object_map: Dict[FrozenSet, Any] = {}
        self.morphism_map: Dict[str, List[Any]] = {}
        # Coordinate assignment for vertices
        self._vertex_coords: Dict[Any, Tuple[float, float, float]] = {}
        self._next_coord = 0

    def assign_vertex_coordinate(self, vertex: Any) -> Tuple[float, float, float]:
        if vertex not in self._vertex_coords:
            # Assign a position on a spiral
            t = self._next_coord * 0.5
            x = t * np.cos(t)
            y = t * np.sin(t)
            z = 0.0
            self._vertex_coords[vertex] = (x, y, z)
            self._next_coord += 1
        return self._vertex_coords[vertex]

    def map_object(self, multiset: Multiset) -> List[Tuple[float, float, float]]:
        """Map a multiset to a list of physical coordinates."""
        key = frozenset(multiset.elements)
        if key in self.object_map:
            return self.object_map[key]
        coords = []
        for vertex, count in multiset.elements:
            coord = self.assign_vertex_coordinate(vertex)
            for _ in range(count):
                coords.append(coord)
        self.object_map[key] = coords
        return coords

    def map_reaction(self, rule_name: str) -> List[Any]:
        """
        Map a reaction rule to a sequence of instructions.

        For G-code: move to reactant positions (extrude), then to product positions.
        For DNA: generate a sequence encoding the transformation.
        """
        if rule_name in self.morphism_map:
            return self.morphism_map[rule_name]

        rule = self.reaction_category.reactions.get(rule_name)
        if rule is None:
            return []

        instructions = []
        if self.output_type == 'gcode':
            # Move to each reactant position and "extrude" (consume)
            for vertex, count in rule.reactants.elements:
                x, y, z = self.assign_vertex_coordinate(vertex)
                for _ in range(count):
                    instructions.append(GCodeInstruction("G0", x=x, y=y, z=z))
                    instructions.append(GCodeInstruction("G1", e=1.0))  # extrude
            # Move to product positions
            for vertex, count in rule.products.elements:
                x, y, z = self.assign_vertex_coordinate(vertex)
                for _ in range(count):
                    instructions.append(GCodeInstruction("G0", x=x, y=y, z=z))
                    instructions.append(GCodeInstruction("G1", e=1.0))

        elif self.output_type == 'dna':
            # Represent the reaction as a DNA edit
            reactant_seq = "".join(str(hash(v))[:4] for v, c in rule.reactants.elements for _ in range(c))
            product_seq = "".join(str(hash(v))[:4] for v, c in rule.products.elements for _ in range(c))
            instructions.append(DNAInstruction(
                sequence=product_seq,
                operation="replace",
                target=reactant_seq
            ))

        self.morphism_map[rule_name] = instructions
        return instructions

    def compile_program(self, reaction_sequence: List[str]) -> List[str]:
        """
        Compile a sequence of reaction names into a list of machine
        instructions (as strings).
        """
        program = []
        for name in reaction_sequence:
            for instr in self.map_reaction(name):
                if isinstance(instr, GCodeInstruction):
                    program.append(instr.to_string())
                elif isinstance(instr, DNAInstruction):
                    program.append(f"{instr.operation} {instr.sequence} at {instr.target}")
        return program


