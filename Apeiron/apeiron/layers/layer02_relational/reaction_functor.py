# Bio-Digital Compiler
# ============================================================================

class BioDigitalCompiler:
    """
    Compiles a hypergraph into a reaction network, then to physical
    instructions. Integrates formal verification to ensure stability.
    """

    def __init__(self, hypergraph, output_type: str = 'gcode'):
        self.hypergraph = hypergraph
        self.output_type = output_type
        self.reaction_cat = ReactionCategory()
        self._build_reactions()

    def _build_reactions(self):
        """Translate hyperedges into reaction rules."""
        for eid, verts in self.hypergraph.hyperedges.items():
            # A hyperedge {v1, ..., vk} becomes a reaction:
            # v1 + ... + vk → v1...vk (a new compound)
            reactants = {v: 1 for v in verts}
            product_name = f"compound_{eid}"
            products = {product_name: 1}
            self.reaction_cat.add_reaction(eid, reactants, products,
                                           rate=self.hypergraph.weights.get(eid, 1.0))

    def compile_to_instructions(self) -> List[str]:
        """Compile all reactions to a physical program."""
        functor = ReactionFunctor(self.reaction_cat, output_type=self.output_type)
        # Topological sort of reactions (if acyclic)
        reaction_list = list(self.reaction_cat.reactions.keys())
        return functor.compile_program(reaction_list)

    def verify_stability(self) -> Dict[str, Any]:
        """
        Verify that the compiled output is consistent with the sheaf
        conditions of the source hypergraph.
        """
        try:
            from .sheaf_hypergraph import SheafHypergraph
            vertices = [f"v_{v}" for v in sorted(self.hypergraph.vertices)]
            hyperedges = [{f"v_{v}" for v in edge} for edge in self.hypergraph.hyperedges.values()]
            if vertices and hyperedges:
                shg = SheafHypergraph(vertices, hyperedges)
                cohom = shg.compute_cohomology()
                return {
                    'stable': cohom.is_globally_consistent,
                    'h1_dimension': cohom.h1_dimension,
                }
        except ImportError:
            pass
        return {'stable': True, 'h1_dimension': 0}


# ============================================================================
# Factory
# ============================================================================

def compile_hypergraph_to_gcode(hypergraph) -> Dict[str, Any]:
    """Full pipeline: hypergraph → G-code."""
    compiler = BioDigitalCompiler(hypergraph, output_type='gcode')
    program = compiler.compile_to_instructions()
    verification = compiler.verify_stability()
    return {
        'program': program,
        'num_instructions': len(program),
        'verification': verification,
    }


