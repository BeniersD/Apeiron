# Multiset as object in the reaction category
# ============================================================================

@dataclass(frozen=True)
class Multiset:
    """
    A multiset (element of a free commutative monoid over vertices).
    Objects in the reaction category are multisets of observables.
    """
    elements: FrozenSet[Tuple[Any, int]]  # (vertex, multiplicity)

    @staticmethod
    def from_counter(counter: Dict[Any, int]) -> 'Multiset':
        return Multiset(frozenset((k, v) for k, v in counter.items() if v > 0))

    @staticmethod
    def empty() -> 'Multiset':
        return Multiset(frozenset())

    def union(self, other: 'Multiset') -> 'Multiset':
        c = self.as_counter()
        for k, v in other.as_counter().items():
            c[k] += v
        return Multiset.from_counter(c)

    def as_counter(self) -> Dict[Any, int]:
        return dict(self.elements)

    def __add__(self, other: 'Multiset') -> 'Multiset':
        return self.union(other)

    def __repr__(self):
        return f"Multiset({dict(self.elements)})"


# ============================================================================
# Reaction rule (generating morphism)
# ============================================================================

@dataclass(frozen=True)
class ReactionRule:
    """
    A reaction rule: reactants → products, consuming and producing
    multisets. This is a generating morphism in the reaction category.
    """
    reactants: Multiset
    products: Multiset
    name: str
    rate: float = 1.0

    def apply(self, state: Multiset) -> Optional[Multiset]:
        """
        Attempt to apply this rule to a state. If the reactants are
        present, return the new state with reactants consumed and
        products added. Otherwise return None.
        """
        state_counter = state.as_counter()
        reactant_counter = self.reactants.as_counter()
        # Check if reactants are present
        for k, v in reactant_counter.items():
            if state_counter.get(k, 0) < v:
                return None
        # Consume reactants
        for k, v in reactant_counter.items():
            state_counter[k] -= v
            if state_counter[k] == 0:
                del state_counter[k]
        # Produce products
        for k, v in self.products.as_counter().items():
            state_counter[k] = state_counter.get(k, 0) + v
        return Multiset.from_counter(state_counter)


# ============================================================================
# Reaction Category (symmetric monoidal category)
# ============================================================================

class ReactionCategory:
    """
    A symmetric monoidal category representing a Petri net of reactions.

    Objects = multisets of vertices (observables).
    Morphisms = finite compositions of reaction rules (including parallel
    application via tensor product).
    """
    def __init__(self):
        self.reactions: Dict[str, ReactionRule] = {}
        self._objects: Set[Multiset] = set()

    def add_reaction(self, name: str, reactants: Dict[Any, int],
                     products: Dict[Any, int], rate: float = 1.0):
        rule = ReactionRule(
            reactants=Multiset.from_counter(reactants),
            products=Multiset.from_counter(products),
            name=name,
            rate=rate
        )
        self.reactions[name] = rule
        self._objects.add(rule.reactants)
        self._objects.add(rule.products)

    def compose(self, rule1: str, rule2: str) -> Optional[str]:
        """
        Compose two reactions sequentially, if the products of the first
        match the reactants of the second. Returns a new rule name or None.
        """
        r1 = self.reactions.get(rule1)
        r2 = self.reactions.get(rule2)
        if r1 is None or r2 is None:
            return None
        # Check if r1's products contain r2's reactants
        p1 = r1.products.as_counter()
        r2r = r2.reactants.as_counter()
        for k, v in r2r.items():
            if p1.get(k, 0) < v:
                return None
        # Compute net effect
        net_reactants = r1.reactants.as_counter()
        for k, v in r2r.items():
            p1[k] -= v
            if p1[k] == 0:
                del p1[k]
        # The overall reactants: r1.reactants + (r2.reactants \ r1.products)
        overall_reactants = {}
        for k, v in r1.reactants.as_counter().items():
            overall_reactants[k] = v
        for k, v in r2.reactants.as_counter().items():
            if k not in p1 or p1.get(k, 0) < v:
                overall_reactants[k] = overall_reactants.get(k, 0) + v - p1.get(k, 0)
        overall_products = {}
        for k, v in r2.products.as_counter().items():
            overall_products[k] = v
        for k, v in r1.products.as_counter().items():
            if k not in r2.reactants.as_counter():
                overall_products[k] = overall_products.get(k, 0) + v
        name = f"{rule1}∘{rule2}"
        self.add_reaction(name, overall_reactants, overall_products)
        return name

    def tensor(self, rule1: str, rule2: str) -> str:
        """
        Parallel composition (tensor product) of two reactions.
        """
        r1 = self.reactions[rule1]
        r2 = self.reactions[rule2]
        reactants = {}
        for k, v in r1.reactants.as_counter().items():
            reactants[k] = v
        for k, v in r2.reactants.as_counter().items():
            reactants[k] = reactants.get(k, 0) + v
        products = {}
        for k, v in r1.products.as_counter().items():
            products[k] = v
        for k, v in r2.products.as_counter().items():
            products[k] = products.get(k, 0) + v
        name = f"{rule1}⊗{rule2}"
        self.add_reaction(name, reactants, products)
        return name


