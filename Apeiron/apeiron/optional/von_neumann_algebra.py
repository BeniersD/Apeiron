"""
Von Neumann Algebra – Non‑Commutative Observational Algebra for APEIRON
========================================================================
Optional module for Layer 2.

Implements a von Neumann algebra over the QuantumGraph, recognizing that
the order of observation affects the structure of knowledge. For certain
pairs of relations, [A, B] ≠ 0. The AI acknowledges that understanding
the "shape" of an object unavoidably disturbs information about its
"change", eliminating the illusion of an objective, static reality.

Mathematical Foundation
-----------------------
A von Neumann algebra M is a *-algebra of bounded operators on a Hilbert
space H, closed in the weak operator topology. Here:
- H is the space of 0‑cochains C⁰(H; F) (functions on vertices).
- Operators are generated by projection operators onto vertex stalks
  and the Dirac operator D from the spectral triple.
- The commutator [A, B] measures non‑simultaneous knowability.

The double commutant theorem M'' = M provides a closure property that
guarantees the algebra is generated by its projections. In Apeiron,
projections correspond to assertions about observable values, and the
non‑commutativity captures the context‑dependence of knowledge.

References
----------
.. [1] von Neumann, J. "Mathematical Foundations of Quantum Mechanics" (1955)
.. [2] Beniers, D. "Quantum Graph Module for APEIRON" (2025)
.. [3] Connes, A. "Noncommutative Geometry" (1994)
"""

