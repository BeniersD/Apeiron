def von_neumann_analysis(hypergraph) -> Dict[str, Any]:
    """
    Perform non‑commutative analysis on a hypergraph.

    Returns a summary of non‑commutativity in the knowledge graph.
    """
    algebra = VonNeumannAlgebra(hypergraph)
    # Check non‑commutativity between Dirac operator and vertex projections
    results = {}
    if algebra.dirac is not None:
        for v, proj in algebra.projections.items():
            comm_check = algebra.check_non_commutativity(proj, algebra.dirac)
            if not comm_check['compatible']:
                results[f"D vs P_{v}"] = comm_check
    graph = algebra.global_commutativity_graph()
    return {
        'dirac_commutators': results,
        'commutativity_graph': graph,
    }